package com.joaoandrade.objetivodiaapp.api.model;

import java.time.LocalDateTime;

public class ArtigoFullModel {
	private Long id;
	private String titulo;
	private String avatarUrl;
	private String autor;
	private String descricao;
	private String conteudo;
	private CategoriaResumoModel categoria;
	private LocalDateTime dataPublicacao;
	private LocalDateTime dataAtualizacao;

	public ArtigoFullModel() {
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getTitulo() {
		return titulo;
	}

	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}

	public String getAvatarUrl() {
		return avatarUrl;
	}

	public void setAvatarUrl(String avatarUrl) {
		this.avatarUrl = avatarUrl;
	}

	public String getAutor() {
		return autor;
	}

	public void setAutor(String autor) {
		this.autor = autor;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	public String getConteudo() {
		return conteudo;
	}

	public void setConteudo(String conteudo) {
		this.conteudo = conteudo;
	}

	public CategoriaResumoModel getCategoria() {
		return categoria;
	}

	public void setCategoria(CategoriaResumoModel categoria) {
		this.categoria = categoria;
	}

	public LocalDateTime getDataPublicacao() {
		return dataPublicacao;
	}

	public void setDataPublicacao(LocalDateTime dataPublicacao) {
		this.dataPublicacao = dataPublicacao;
	}

	public LocalDateTime getDataAtualizacao() {
		return dataAtualizacao;
	}

	public void setDataAtualizacao(LocalDateTime dataAtualizacao) {
		this.dataAtualizacao = dataAtualizacao;
	}

}
